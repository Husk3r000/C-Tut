#include<iostream>;
using namespace std;

int main()
{
    int num1, num2;
    cout<<"Enter the value of Num1:\n"; //<<this is called insertion operator
    cin>>num1;                          //>>this is called Extration operator

    cout<<"Enter the value of Num2:\n"; //<<this is called insertion operator
    cin>>num2;                          //>>this is called Extration operator

    cout<<"The sum is "<< num1+num2;

    return 0;
}
